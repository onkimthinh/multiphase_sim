function [krg, krog, Pcgo] = Sg_table(Sg)
% Look up and extrapolate the Gas Saturation Table
    format long;
    
    Table = [0	    0	      1	        0
             0.04	0.01103	  0.70778	0.01
             0.08	0.02912	  0.55844	0.062
             0.12	0.05138	  0.4454	0.114
             0.16	0.07687	  0.35562	0.166
             0.2	0.10506	  0.28302	0.218
             0.24	0.13561	  0.22392	0.27
             0.28	0.16827	  0.17574	0.366
             0.32	0.20286	  0.13656	0.462
             0.36	0.23923	  0.10485	0.580666667
             0.4	0.27725	  0.07938	0.722
             0.44	0.31683	  0.05912	0.863333333
             0.48	0.35788	  0.04319	1.004666667
             0.52	0.40031	  0.03084	1.175
             0.56	0.44408	  0.02143	1.355
             0.6	0.48911	  0.01442	1.563
             0.64	0.53536	  0.00933	1.855
             0.68	0.58279	  0.00574	2.147
             0.72	0.63134	  0.00332	2.652];
%      krg = 165.7*exp(0.7023*Sg) - 165.7*exp(0.6989*Sg);
%      krog = -3.055e-5*exp(8.028*Sg) + 0.9636*exp(-6.32*Sg);
%      Pcgo = 0.114*exp(4.37*Sg);
    for i = 1 : numel(Table(:,1)) - 1
        while Sg >= Table(i, 1) && Sg < Table(i+1, 1)
            ratio = (Sg - Table(i,1))/(Table(i+1,1) - Table(i,1));
            krg = ratio*(Table(i+1,2)-Table(i,2)) + Table(i,2);
            krog = ratio*(Table(i+1,3)-Table(i,3)) + Table(i,3);
            Pcgo = ratio*(Table(i+1,4)-Table(i,4)) + Table(i,4);
            break
        end
    end
end